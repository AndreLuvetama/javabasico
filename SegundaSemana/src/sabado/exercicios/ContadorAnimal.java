package sabado.exercicios;

public class ContadorAnimal {
	
	private String cachorro;
	private String gato;
	private String galinha;
	
	public ContadorAnimal() {}

	public ContadorAnimal(String cachorro, String gato, String galinha) {
		this.cachorro = cachorro;
		this.gato = gato;
		this.galinha = galinha;
	}

	public String getCachorro() {
		return cachorro;
	}

	public void setCachorro(String cachorro) {
		this.cachorro = cachorro;
	}

	public String getGato() {
		return gato;
	}

	public void setGato(String gato) {
		this.gato = gato;
	}

	public String getGalinha() {
		return galinha;
	}

	public void setGalinha(String galinha) {
		this.galinha = galinha;
	}
	
	
	

	
	
	

}
